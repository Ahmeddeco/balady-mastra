export function slugifyTitle(text: string): string {
  if (!text) return ""

  return text
    .toString()
    .toLowerCase()
    .trim()
    // استبدال المسافات والشرطات المتكررة بشرطة واحدة
    .replace(/\s+/g, "-")
    // حذف أي رموز خاصة أو علامات ترقيم مع الحفاظ على الحروف العربية، الإنجليزية، والأرقام
    // النطاق \u0600-\u06FF يغطي جميع الحروف العربية
    .replace(/[^\u0600-\u06FFa-zA-Z0-9\-]/g, "")
    // إزالة الشرطات الزائدة من البداية والنهاية إن وجدت
    .replace(/^-+|-+$/g, "")
    // منع تكرار الشرطات في المنتصف (مثل: --- تتحول إلى -)
    .replace(/-+/g, "-")
}